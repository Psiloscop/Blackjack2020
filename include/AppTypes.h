#pragma once

//! 8 bit unsigned variable.
/** This is a typedef for unsigned char. */
typedef unsigned char u8;

//! 8 bit signed variable.
/** This is a typedef for signed char. */
typedef signed char s8;

//! 8 bit character variable.
/** This is a typedef for char. */
typedef char c8;

//! 16 bit unsigned variable.
/** This is a typedef for unsigned short. */
typedef unsigned short u16;

//! 16 bit signed variable.
/** This is a typedef for signed short. */
typedef signed short s16;

//! 32 bit unsigned variable.
/** This is a typedef for unsigned int. */
typedef unsigned int u32;

//! 32 bit signed variable.
/** This is a typedef for signed int. */
typedef signed int s32;

//! 64 bit unsigned variable.
/** This is a typedef for 64bit uint. */
typedef unsigned long int u64;

//! 64 bit signed variable.
/** This is a typedef for 64bit int. */
typedef long long s64;

//! 32 bit floating point variable.
/** This is a typedef for float. */
typedef float f32;

//! 64 bit floating point variable.
/** This is a typedef for double. */
typedef double f64;